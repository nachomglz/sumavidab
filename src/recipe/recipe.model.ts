import mongoose, { Schema } from 'mongoose'
import type { Recipe as TRecipe } from '../utils/types'

const RecipeSchema = new Schema<TRecipe>({
    name: {
        type: String,
        required: true,
    },
    ingredients: {
        type: [String],
        required: true,
    },
    steps: {
        type: [String],
        required: true,
    },
    image_url: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        required: true,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
    },
})

export default mongoose.model('Recipe', RecipeSchema)
